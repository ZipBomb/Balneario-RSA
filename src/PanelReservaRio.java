    private void botonReservarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonReservarMouseClicked
        // Se polo menos un dos checkbox está marcado mostramos unha ventá
        // emerxente de confirmación. En caso contrario, mostra unha ventá
        // cunha mensaxe de erro
        if (selector1.isSelected() || selector2.isSelected()) {
            Object[] opcions = {"Si", "Non"};
            int resposta = JOptionPane.showOptionDialog(this,
                "Confirma que desexa formalizar a reserva con esta información?",
                "Confirmar proceso de reserva",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.WARNING_MESSAGE,
                null,
                opcions,
                null);

            if (resposta == JOptionPane.YES_OPTION) {
                t.stop();
                
                // Obtemos a descrición e o custo das actividades elixidas polo primeiro usuario
                String desc = "";
                float cantidadeParcial = 0;
                if(boton11.isSelected()) {
                    if(boton12.isSelected() || boton13.isSelected())
                        desc += "Paseo + ";
                    else
                        desc += "Paseo";
                    cantidadeParcial += 14;
                }
                if(boton12.isSelected()){
                    if(boton13.isSelected())
                        desc += "Rafting + ";
                    else
                        desc += "Rafting";
                    cantidadeParcial += 30;                    
                }
                if(boton13.isSelected()){
                    desc += "Descenso";
                    cantidadeParcial += 45;                    
                }
                
                if(selector1.isSelected()) {
                    String data = campoData.getSelectedItem().toString();
                    Actividade a = new Actividade(data, desc, cantidadeParcial);
                    d.engadirActividade(d.getUsuario(0), a);
                }
                
                // Obtemos a descrición e o custo das actividades elixidas polo segundo usuario                  
                desc = "";
                cantidadeParcial = 0;              
                if(boton21.isSelected()) {
                    if(boton22.isSelected() || boton23.isSelected())
                        desc += "Paseo + ";
                    else
                        desc += "Paseo";
                    cantidadeParcial += 14;
                }
                if(boton22.isSelected()){
                    if(boton23.isSelected())
                        desc += "Rafting + ";
                    else
                        desc += "Rafting";
                    cantidadeParcial += 30;                    
                }
                if(boton23.isSelected()){
                    desc += "Descenso";
                    cantidadeParcial += 45;                    
                }                
                if(selector2.isSelected()) {
                    String data = campoData.getSelectedItem().toString();
                    Actividade a = new Actividade(data, desc, cantidadeParcial);
                    d.engadirActividade(d.getUsuario(1), a);
                }
                this.v.getContentPane().setVisible(false);
                PanelReservaCompletada prc = new PanelReservaCompletada(d);
                prc.setVisible(true);
                prc.setV(this.v);
                this.v.setContentPane(prc);
            }
            else {
                t.restart();
            }
        }
        else {
            t.restart();
            JOptionPane.showMessageDialog(this,
                "Debe marcar polo menos unha opción",
                "Erro",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonReservarMouseClicked

