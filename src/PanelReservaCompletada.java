    public PanelReservaCompletada(DatosReserva d) {
        initComponents();
        
        // Declaramos las variables para los iconos de los altavoces
        // e iniciamos el contador de tiempo de inactividad
        iconoAudioOn = new javax.swing.ImageIcon(urlAudioOn);
                iconoAudioOff = new javax.swing.ImageIcon(urlAudioOff);        
        
        int tiempoEspera = 4 * 60 * 1000;
        ActionListener taskPerformer = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent evt) {
                v.getContentPane().setVisible(false);
                PanelInactive pi = new PanelInactive(d);
                pi.setVisible(true);
                pi.setV(v);
                v.setContentPane(pi);                 
            }
        };
        t = new Timer(tiempoEspera, taskPerformer);
        t.start();       
        formato = new DecimalFormat("0.00");
        
        if(d != null) {
            this.d = d;
        }
        
        HashMap<Participante, Actividade> a = d.getActividades();
        if(a != null) {   
            float total = 0;
            int i = 0;
            for(HashMap.Entry<Participante, Actividade> entrada : a.entrySet()){
                if(i == 0) {
                    campoData.setText(entrada.getValue().getData());
                    campoParticipante1.setText(entrada.getKey().getNome() + " con DNI " + entrada.getKey().getDni());
                    campoImporte1.setText(entrada.getValue().getDescricion() + " ->      " + formato.format(entrada.getValue().getPrezo()) + " €");
                    total += entrada.getValue().getPrezo();
                    i++;
                } else if(i == 1) {
                    campoParticipante2.setText(entrada.getKey().getNome() + " con DNI " + entrada.getKey().getDni());
                    campoImporte2.setText(entrada.getValue().getDescricion() + " ->      " + formato.format(entrada.getValue().getPrezo()) + " €");
                    total += entrada.getValue().getPrezo();                    
                    i++;                    
                } else {
                    break;
                }
            }
            campoImporteTotal.setText(formato.format(total) + "€ ");
        }
    }
   

