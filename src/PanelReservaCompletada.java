    private void botonAtrasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonAtrasMouseClicked
        // TODO add your handling code here:               
        Object[] opcions = {"Si", "Non"};
        int resposta = JOptionPane.showOptionDialog(this,
                       "Seguro que quere saír? Se sae agora perderase a información cuberta",
                       "Cancelar proceso de reserva",
                       JOptionPane.YES_NO_CANCEL_OPTION,
                       JOptionPane.WARNING_MESSAGE,
                       null,
                       opcions,
                       null);

        if (resposta == JOptionPane.YES_OPTION) {
            t.stop();
            d.reseteaActividades();
            this.v.getContentPane().setVisible(false);
            PanelMain pm = new PanelMain(d);
            pm.setVisible(true);
            pm.setV(this.v);
            this.v.setContentPane(pm);
        }          
    }//GEN-LAST:event_botonAtrasMouseClicked

