    private void boton11ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_boton11ItemStateChanged
        // TODO add your handling code here:
        if (!selector1.isSelected()) {
            selector1.setSelected(true);
        }
        if (boton11.isSelected()) {
            cantidad += 14;
        }
        else {
            cantidad -= 14;
        }
        if(!boton11.isSelected() && !boton12.isSelected() && !boton13.isSelected())
            selector1.setSelected(false);
            
        importe.setText(formato.format(cantidad) + " €");
        t.restart();        
    }//GEN-LAST:event_boton11ItemStateChanged

